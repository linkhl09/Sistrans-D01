FROM( 
    SELECT VALORTOTAL, 
        FECHA, 
        IDSUCURSAL
    FROM FACTURA WHERE EXTRACT(YEAR FROM FECHA) = EXTRACT(YEAR FROM (SELECT SYSDATE FROM DUAL))
        AND FECHA BETWEEN 'DD/MM/YY' AND 'DD/MM/YY' AND IDSUCURSAL = ?
    );